def positif_ou_negatif(nombre):
    if nombre > 0:
        print("positif")
    elif nombre < 0:
        print("negatif")
    else:
        print("nul")


positif_ou_negatif(5)  # Affiche "positif"
positif_ou_negatif(-2) # Affiche "negatif"
positif_ou_negatif(0)  # Affiche "nul"
positif_ou_negatif(10) # Affiche "positif"
positif_ou_negatif(-7) # Affiche "negatif"